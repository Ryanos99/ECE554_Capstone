        wait (S_AXI_RVALID);
        data = S_AXI_RDATA;

        @(posedge S_AXI_ACLK);
        S_AXI_RREADY <= 0;
    end
    endtask

    task automatic load_frame(input int frame_index);
        $display("\n[TB] Loading frame %0d at time %0t", frame_index, $time);
 
        for (int i = 1; i < (NUM_INPUTS + 1); i++) begin
            axi_write(i*4, {24'd0, frames[frame_index][i-1]});
            //$display("\n[TB] Loading frame[%0d][%0d] with %0d", frame_index, i-1, frames[frame_index][i-1]);
        end

        // trigger start (adjust address if needed)
        axi_write(8'h90, 0);
        @(posedge S_AXI_ACLK);
        axi_write(8'h8c, 1);
        @(posedge S_AXI_ACLK);
        axi_write(8'h8c, 0);
    endtask

    reg [31:0] status;
    task automatic wait_for_done();
        //reg [31:0] status;
        begin
            while (1) begin
                axi_read(8'h90, status); // done register
                if (status == 1) break;
                @(posedge S_AXI_ACLK);
            end
        end
    endtask

    task automatic print_outputs();
        reg [31:0] result;
        begin
            axi_read(8'h94, result);

            $display("[TB] MLP output:");
            case(result[1:0])
                2'b00: $display(" BENCH");
                2'b01: $display(" PUSHUP");
                2'b10: $display(" SQUAT");
                default: $display(" IDLE");
            endcase
        end
    endtask

    initial begin
        // init AXI signals
        S_AXI_ACLK    = 0;
        S_AXI_ARESETN = 0;

        S_AXI_AWVALID = 0;
        S_AXI_WVALID  = 0;
        S_AXI_BREADY  = 0;
        S_AXI_ARVALID = 0;
        S_AXI_RREADY  = 0;

        S_AXI_AWPROT = 0;
        S_AXI_ARPROT = 0;

        // reset
        repeat(5) @(posedge S_AXI_ACLK);
        S_AXI_ARESETN = 1;

        repeat(2) @(posedge S_AXI_ACLK);

        // run frames
        for (int f = 0; f < NUM_FRAMES; f++) begin
            load_frame(f);
            wait_for_done();
            print_outputs();
            repeat(5) @(posedge S_AXI_ACLK);
        end

        $display("\n[TB] All frames complete.");
        $stop();
    end

    always
        #5 S_AXI_ACLK = ~S_AXI_ACLK;

endmodule
